package basic_variable_type;

public class 논리타입boolean {
    public static void main(String[] args) {
        // boolean z = true
        // boolean x = false
        // 제어문의 실행 흐름을 변경하는데 사용한다

        boolean a = true;
        //if 를사용 하여 true일경우 실행 false일경우 else 이후의 문구 실행을 한다

        if(a){
            System.out.println("\"중지합니다\"");

        } else {System.out.println("\"시작합니다\"");

        }

        //if 를사용 하여 true일경우 실행 false일경우 else 이후의 문구 실행을 한다
        boolean b = false;

        if(b){
            System.out.println("\"중지합니다\"");

        } else {System.out.println("\"시작합니다\"");

        }



    }
}
